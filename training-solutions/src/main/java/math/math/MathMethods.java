package math.math;

public class MathMethods {

    public static void main(String[] args) {

        System.out.println(Math.max(11, 25.5));
        System.out.println(Math.min(-12, -77));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(24.2));
        System.out.println(Math.abs(-63.25));
        System.out.println(Math.negateExact(10));
        System.out.println(Math.addExact(-9, 80));
        System.out.println(Math.subtractExact(-12, 60));
        System.out.println(Math.multiplyExact(25, 25));
        System.out.println(Math.pow(10.1, 2.0));
        System.out.println(Math.incrementExact(-21));
        System.out.println(Math.decrementExact(99));
        System.out.println(Math.random());
    }
}
